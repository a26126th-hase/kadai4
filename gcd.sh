#!/bin/bash
# gcd.sh - 2つの自然数(1以上の整数)の最大公約数を出力する
# 使い方: ./gcd.sh <自然数1> <自然数2>
# 正常時: 標準出力にGCDを出力し、終了コード0
# 異常時: 標準エラーにメッセージを出力し、終了コード1

set -u

# 引数は2つ
if [ "$#" -ne 2 ]; then
    echo "Error: 引数は2つ必要です。使い方: $0 <自然数1> <自然数2>" >&2
    exit 1
fi

a="$1"
b="$2"

# 自然数(1以上の整数)のみ許可。0・負数・小数・文字列はすべてエラー
if ! [[ "$a" =~ ^[1-9][0-9]*$ ]] || ! [[ "$b" =~ ^[1-9][0-9]*$ ]]; then
    echo "Error: 引数は1以上の自然数で指定してください" >&2
    exit 1
fi

# ユークリッドの互除法
while [ "$b" -ne 0 ]; do
    tmp="$b"
    b=$(( a % b ))
    a="$tmp"
done

echo "$a"
exit 0

